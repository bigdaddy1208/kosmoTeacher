package dto;

public class CalendarDTO {
	private String year;
	private String mon;
	private String day;
	private int beginBlank;
	private int lastDate;
	private int endBlank;
	private int totalTd;
	
	
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMon() {
		return mon;
	}
	public void setMon(String mon) {
		this.mon = mon;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public int getBeginBlank() {
		return beginBlank;
	}
	public void setBeginBlank(int beginBlank) {
		this.beginBlank = beginBlank;
	}
	public int getLastDate() {
		return lastDate;
	}
	public void setLastDate(int lastDate) {
		this.lastDate = lastDate;
	}
	public int getEndBlank() {
		return endBlank;
	}
	public void setEndBlank(int endBlank) {
		this.endBlank = endBlank;
	}
	public int getTotalTd() {
		return totalTd;
	}
	public void setTotalTd(int totalTd) {
		this.totalTd = totalTd;
	}
	
	


	
}
